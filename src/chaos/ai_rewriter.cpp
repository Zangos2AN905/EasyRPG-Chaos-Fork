// Escape a string for embedding in JSON
std::string JsonEscape(const std::string& s) {
	std::string out;
	out.reserve(s.size() + 16);
	for (char c : s) {
		switch (c) {
			case '"':  out += "\\\""; break;
			case '\\': out += "\\\\"; break;
			case '\n': out += "\\n";  break;
			case '\r': out += "\\r";  break;
			case '\t': out += "\\t";  break;
			default:   out += c;      break;
		}
	}
	return out;
}

// Extract the value of "content" from the first choice in the response JSON.
// This is a deliberately minimal parser — we only need one field.
std::string ExtractContent(const std::string& json) {
	// Find "choices" array, then "message", then "content"
	const std::string key = "\"content\"";
	// We want the content inside choices[0].message.content
	auto choices_pos = json.find("\"choices\"");
	if (choices_pos == std::string::npos) return {};

	auto content_pos = json.find(key, choices_pos);
	if (content_pos == std::string::npos) return {};

	// Find the colon after "content"
	auto colon = json.find(':', content_pos + key.size());
	if (colon == std::string::npos) return {};

	// Skip whitespace to find opening quote
	auto start = json.find('"', colon + 1);
	if (start == std::string::npos) return {};
	++start; // skip the quote

	// Read until unescaped closing quote
	std::string result;
	for (size_t i = start; i < json.size(); ++i) {
		if (json[i] == '\\' && i + 1 < json.size()) {
			char next = json[i + 1];
			switch (next) {
				case '"':  result += '"';  break;
				case '\\': result += '\\'; break;
				case 'n':  result += '\n'; break;
				case 'r':  result += '\r'; break;
				case 't':  result += '\t'; break;
				default:   result += next; break;
			}
			++i;
		} else if (json[i] == '"') {
			break;
		} else {
			result += json[i];
		}
	}
	return result;
}

// Split text by newlines, trimming empty trailing lines
std::vector<std::string> SplitLines(const std::string& text) {
	std::vector<std::string> lines;
	std::istringstream stream(text);
	std::string line;
	while (std::getline(stream, line)) {
		// Trim trailing \r
		if (!line.empty() && line.back() == '\r') {
			line.pop_back();
		}
		lines.push_back(std::move(line));
	}
	// Remove empty trailing lines
	while (!lines.empty() && lines.back().empty()) {
		lines.pop_back();
	}
	return lines;
}

// Extract leading whitespace from a line. Returns {whitespace, rest}.
std::pair<std::string, std::string> SplitLeadingWhitespace(const std::string& line) {
	size_t pos = 0;
	while (pos < line.size() && (line[pos] == ' ' || line[pos] == '\t')) {
		++pos;
	}
	return {line.substr(0, pos), line.substr(pos)};
}

// Strip RPG Maker text formatting codes (\C[n], \N[n], \V[n], \S[n], \!, etc.)
std::string StripFormatCodes(const std::string& text) {
	std::string result;
	for (size_t i = 0; i < text.size(); ++i) {
		if ((text[i] == '\\' || text[i] == '$') && i + 1 < text.size()) {
			char next = text[i + 1];
			// Bracket parameter: \X[n]
			if (std::isalpha(static_cast<unsigned char>(next)) &&
				i + 2 < text.size() && text[i + 2] == '[') {
				auto end = text.find(']', i + 3);
				if (end != std::string::npos) {
					i = end;
					continue;
				}
			}
			// Single-char codes
			if (next == '!' || next == '.' || next == '|' || next == '>' ||
				next == '<' || next == '^' || next == '\\' || next == '$' || next == '_') {
				++i;
				continue;
			}
		}
		result += text[i];
	}
	return result;
}

// Word-wrap a single line, breaking at word boundaries.
std::vector<std::string> WordWrap(const std::string& prefix, const std::string& text, bool has_face) {
	std::vector<std::string> result;
	int char_limit = has_face ? MAX_CHARS_PER_LINE_FACE : MAX_CHARS_PER_LINE;
	int max_content = char_limit - static_cast<int>(prefix.size());
	if (max_content < 10) max_content = 10; // safety floor

	std::string remaining = text;
	while (!remaining.empty()) {
		if (static_cast<int>(remaining.size()) <= max_content) {
			result.push_back(prefix + remaining);
			break;
		}
		// Find last space within limit
		int cut = max_content;
		while (cut > 0 && remaining[cut] != ' ') {
			--cut;
		}
		if (cut == 0) cut = max_content; // no space found, hard break

		result.push_back(prefix + remaining.substr(0, cut));
		// Skip the space at the break point
		size_t next = cut;
		while (next < remaining.size() && remaining[next] == ' ') ++next;
		remaining = remaining.substr(next);
	}
	if (result.empty()) result.push_back(prefix);
	return result;
}

// Split a block of lines into 4-line message box chunks.
std::vector<std::vector<std::string>> SplitIntoBoxes(const std::vector<std::string>& lines) {
	std::vector<std::vector<std::string>> boxes;
	for (size_t i = 0; i < lines.size(); i += MAX_LINES_PER_BOX) {
		std::vector<std::string> box;
		for (size_t j = i; j < lines.size() && j < i + MAX_LINES_PER_BOX; ++j) {
			box.push_back(lines[j]);
		}
		boxes.push_back(std::move(box));
	}
	if (boxes.empty()) boxes.push_back({});
	return boxes;
}

