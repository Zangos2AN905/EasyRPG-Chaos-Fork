// Read a JSON string value by key (first occurrence in the given text)
std::string JsonReadString(const std::string& json, const std::string& key) {
	std::string search = "\"" + key + "\"";
	auto pos = json.find(search);
	if (pos == std::string::npos) return {};

	auto colon = json.find(':', pos + search.size());
	if (colon == std::string::npos) return {};

	auto start = json.find('"', colon + 1);
	if (start == std::string::npos) return {};
	++start;

	std::string result;
	for (size_t i = start; i < json.size(); ++i) {
		if (json[i] == '\\' && i + 1 < json.size()) {
			char next = json[i + 1];
			switch (next) {
				case '"':  result += '"';  break;
				case '\\': result += '\\'; break;
				case 'n':  result += '\n'; break;
				case 'r':  break;
				case 't':  result += '\t'; break;
				default:   result += next;  break;
			}
			++i;
		} else if (json[i] == '"') {
			break;
		} else {
			result += json[i];
		}
	}
	return result;
}

// Read a JSON integer value by key
int64_t JsonReadInt(const std::string& json, const std::string& key) {
	std::string search = "\"" + key + "\"";
	auto pos = json.find(search);
	if (pos == std::string::npos) return 0;

	auto colon = json.find(':', pos + search.size());
	if (colon == std::string::npos) return 0;

	size_t start = colon + 1;
	while (start < json.size() && (json[start] == ' ' || json[start] == '\t' || json[start] == '\n')) ++start;

	return std::strtoll(json.c_str() + start, nullptr, 10);
}

// Find the end of a JSON object starting at '{', accounting for strings
size_t FindJsonObjectEnd(const std::string& json, size_t start) {
	if (start >= json.size() || json[start] != '{') return std::string::npos;

	int depth = 0;
	bool in_string = false;
	for (size_t i = start; i < json.size(); ++i) {
		if (in_string) {
			if (json[i] == '\\') { ++i; continue; }
			if (json[i] == '"') in_string = false;
		} else {
			if (json[i] == '"') in_string = true;
			else if (json[i] == '{') ++depth;
			else if (json[i] == '}') { --depth; if (depth == 0) return i; }
		}
	}
	return std::string::npos;
}

// Parse the "entries" array from memory JSON
std::vector<MemoryEntry> JsonReadEntries(const std::string& json) {
	std::vector<MemoryEntry> entries;

	auto arr_key = json.find("\"entries\"");
	if (arr_key == std::string::npos) return entries;

	auto arr_start = json.find('[', arr_key);
	if (arr_start == std::string::npos) return entries;

	// Find matching ]
	int depth = 1;
	size_t arr_end = arr_start + 1;
	bool in_str = false;
	while (arr_end < json.size() && depth > 0) {
		if (in_str) {
			if (json[arr_end] == '\\') { ++arr_end; }
			else if (json[arr_end] == '"') in_str = false;
		} else {
			if (json[arr_end] == '"') in_str = true;
			else if (json[arr_end] == '[') ++depth;
			else if (json[arr_end] == ']') --depth;
		}
		++arr_end;
	}

	// Parse individual objects
	size_t pos = arr_start + 1;
	while (pos < arr_end) {
		auto obj_start = json.find('{', pos);
		if (obj_start == std::string::npos || obj_start >= arr_end) break;

		auto obj_end = FindJsonObjectEnd(json, obj_start);
		if (obj_end == std::string::npos) break;

		std::string obj = json.substr(obj_start, obj_end - obj_start + 1);

		MemoryEntry e;
		e.original = JsonReadString(obj, "original");
		e.rewritten = JsonReadString(obj, "rewritten");
		e.timestamp = JsonReadInt(obj, "timestamp");
		entries.push_back(std::move(e));

		pos = obj_end + 1;
	}

	return entries;
}

// ── Memory file path helpers ───────────────────────────────────────────────────

bool MakeDirectoryRecursive(const std::string& path) {
	size_t pos = 0;
	while (pos < path.size()) {
		pos = path.find('/', pos + 1);
		if (pos == std::string::npos) pos = path.size();
		std::string sub = path.substr(0, pos);
		if (!sub.empty()) {
			mkdir(sub.c_str(), 0755); // ignore errors for existing dirs
		}
	}
	struct stat st;
	return stat(path.c_str(), &st) == 0 && S_ISDIR(st.st_mode);
}

std::string GetMemoryDir() {
	const char* xdg = std::getenv("XDG_CONFIG_HOME");
	std::string dir;
	if (xdg && xdg[0]) {
		dir = xdg;
	} else {
		const char* home = std::getenv("HOME");
		if (home) {
			dir = std::string(home) + "/.config";
		} else {
			return {};
		}
	}
	return dir + "/EasyRPG/Player";
}

std::string GetMemoryPath() {
	std::string dir = GetMemoryDir();
	if (dir.empty()) return {};
	return dir + "/ai_memory.json";
}

void SaveMemoryToFile() {
	std::string path = GetMemoryPath();
	if (path.empty()) return;

	std::string dir = GetMemoryDir();
	if (!MakeDirectoryRecursive(dir)) {
		Output::Warning("Chaos AI Memory: failed to create directory {}", dir);
		return;
	}

	std::ofstream file(path);
	if (!file.is_open()) {
		Output::Warning("Chaos AI Memory: failed to open {} for writing", path);
		return;
	}

	file << "{\n";
	file << "  \"version\": 1,\n";
	file << "  \"session_count\": " << session_count << ",\n";
	file << "  \"notes\": \"" << JsonEscape(memory_notes) << "\",\n";
	file << "  \"entries\": [\n";
	for (size_t i = 0; i < long_term_memory.size(); ++i) {
		file << "    {\"original\": \"" << JsonEscape(long_term_memory[i].original)
			 << "\", \"rewritten\": \"" << JsonEscape(long_term_memory[i].rewritten)
			 << "\", \"timestamp\": " << long_term_memory[i].timestamp << "}";
		if (i + 1 < long_term_memory.size()) file << ",";
		file << "\n";
	}
	file << "  ]\n";
	file << "}\n";

	file.close();
	Output::Debug("Chaos AI Memory: saved {} entries to {}", long_term_memory.size(), path);
}

void LoadMemoryFromFile() {
	if (memory_loaded) return;
	memory_loaded = true;

	std::string path = GetMemoryPath();
	if (path.empty()) return;

	std::ifstream file(path);
	if (!file.is_open()) {
		Output::Debug("Chaos AI Memory: no memory file at {} (starting fresh)", path);
		return;
	}

	std::string json((std::istreambuf_iterator<char>(file)),
					  std::istreambuf_iterator<char>());
	file.close();

	session_count = static_cast<int>(JsonReadInt(json, "session_count"));
	memory_notes = JsonReadString(json, "notes");
	long_term_memory = JsonReadEntries(json);

	Output::Debug("Chaos AI Memory: loaded {} entries, {} sessions, notes: {}chars",
		long_term_memory.size(), session_count, memory_notes.size());
}

// Compact a multi-line dialogue block into a single summary line
std::string CompactDialogue(const std::vector<std::string>& lines) {
	std::string result;
	for (size_t i = 0; i < lines.size(); ++i) {
		if (i > 0) result += " / ";
		result += lines[i];
	}
	return result;
}

// Build memory context string for injection into the AI prompt
std::string BuildMemoryContext() {
	std::string context;

	// Add notes if present
	if (!memory_notes.empty()) {
		context += "\nPREVIOUS SESSION NOTES (maintain these themes/style):\n";
		std::string notes = memory_notes;
		if (notes.size() > 500) notes = notes.substr(0, 497) + "...";
		context += notes + "\n";
	}

	// Add recent short-term entries (this session)
	if (!short_term_memory.empty()) {
		context += "\nRECENT DIALOGUE THIS SESSION (maintain continuity):\n";
		size_t start_idx = short_term_memory.size() > 5 ? short_term_memory.size() - 5 : 0;
		for (size_t i = start_idx; i < short_term_memory.size(); ++i) {
			std::string orig = short_term_memory[i].original;
			std::string rewr = short_term_memory[i].rewritten;
			if (orig.size() > 150) orig = orig.substr(0, 147) + "...";
			if (rewr.size() > 150) rewr = rewr.substr(0, 147) + "...";
			context += "- Original: " + orig + "\n  Rewritten: " + rewr + "\n";
		}
	}

	// Add long-term entries (from past sessions)
	if (!long_term_memory.empty()) {
		context += "\nDIALOGUE FROM PREVIOUS SESSIONS (for style consistency):\n";
		size_t start_idx = long_term_memory.size() > 5 ? long_term_memory.size() - 5 : 0;
		for (size_t i = start_idx; i < long_term_memory.size(); ++i) {
			std::string orig = long_term_memory[i].original;
			std::string rewr = long_term_memory[i].rewritten;
			if (orig.size() > 150) orig = orig.substr(0, 147) + "...";
			if (rewr.size() > 150) rewr = rewr.substr(0, 147) + "...";
			context += "- Original: " + orig + "\n  Rewritten: " + rewr + "\n";
		}
	}

	return context;
}

// Update memory after a successful rewrite
void UpdateMemoryAfterRewrite() {
	for (auto& entry : cached_entries) {
		if (entry.rewritten.empty()) continue;

		MemoryEntry mem;
		mem.original = CompactDialogue(entry.original);
		mem.rewritten = CompactDialogue(entry.rewritten);
		mem.timestamp = static_cast<int64_t>(std::time(nullptr));

		// Add to short-term
		short_term_memory.push_back(mem);
		if (short_term_memory.size() > MAX_SHORT_TERM) {
			short_term_memory.erase(short_term_memory.begin());
		}

		// Add to long-term
		long_term_memory.push_back(std::move(mem));
		if (long_term_memory.size() > MAX_LONG_TERM) {
			long_term_memory.erase(long_term_memory.begin());
		}
	}

	SaveMemoryToFile();
}

